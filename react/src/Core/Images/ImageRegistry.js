// Helper function to get image source for a given key
export const getImageSource = (imageKey) => {
  if (imageKey.startsWith("M-")) return mainPhotos[imageKey];
  if (imageKey.startsWith("C-")) return closePhotos[imageKey];
  return otherPhotos[imageKey];
};

// Helper to get main photo for a structure number
export const getMainPhoto = (structureNumber) => {
  return mainPhotos[`M-${structureNumber}`];
};
